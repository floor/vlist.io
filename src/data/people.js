/** Flattened, id'd, name-sorted people dataset. */
export const PEOPLE = (() => {
  const out = [];
  let id = 1;
  for (const category of Object.keys(PEOPLE_SOURCE)) {
    for (const [name, country, year] of PEOPLE_SOURCE[category]) {
      out.push({ id: id++, name, category, country, year });
    }
  }
  out.sort((a, b) => a.name.localeCompare(b.name));
  return out;
})();
