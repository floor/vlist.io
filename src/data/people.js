// Text Color (WCAG contrast)
// =============================================================================

/**
 * Relative luminance of a hex color (WCAG 2.x definition).
 * @param {string} hex - e.g. "#e53935" or "e53935"
 * @returns {number} 0–1
 */
const luminance = (hex) => {
  const c = hex.charAt(0) === "#" ? hex.substring(1, 7) : hex;
  const sRGB = [
    parseInt(c.substring(0, 2), 16) / 255,
    parseInt(c.substring(2, 4), 16) / 255,
    parseInt(c.substring(4, 6), 16) / 255,
  ];
  const lin = sRGB.map((v) =>
    v <= 0.03928 ? v / 12.92 : Math.pow((v + 0.055) / 1.055, 2.4),
  );
  return 0.2126 * lin[0] + 0.7152 * lin[1] + 0.0722 * lin[2];
};

/**
 * WCAG contrast ratio between two luminance values.
 * @param {number} l1
 * @param {number} l2
 * @returns {number}
 */
const contrastRatio = (l1, l2) => {
  const lighter = Math.max(l1, l2);
  const darker = Math.min(l1, l2);
  return (lighter + 0.05) / (darker + 0.05);
};

/**
 * Return the text color (light or dark) that gives the highest WCAG contrast
 * ratio against the given background. Guarantees the best possible readability.
 * @param {string} bgColor - Hex color string (e.g. "#e53935")
 * @param {string} [lightColor='#ffffff']
 * @param {string} [darkColor='#000000']
 * @returns {string}
 */
export const textColor = (bgColor, lightColor, darkColor) => {
  lightColor = lightColor || "#ffffff";
  darkColor = darkColor || "#000000";

  const bgL = luminance(bgColor);
  const whiteContrast = contrastRatio(bgL, 1); // luminance of #fff = 1
  const blackContrast = contrastRatio(bgL, 0); // luminance of #000 = 0

  return blackContrast >= whiteContrast ? darkColor : lightColor;
};

// =============================================================================
// Deterministic Hash
// =============================================================================

/**
 * FNV-1a hash — same index + seed always produces the same output.
 * @param {number} index
 * @param {number} [seed=0]
 * @returns {number} Unsigned 32-bit integer
 */
export const hash = (index, seed = 0) => {
  let h = 2166136261 ^ seed;
  h = Math.imul(h ^ (index & 0xff), 16777619);
  h = Math.imul(h ^ ((index >> 8) & 0xff), 16777619);
  h = Math.imul(h ^ ((index >> 16) & 0xff), 16777619);
  h = Math.imul(h ^ ((index >> 24) & 0xff), 16777619);
  return h >>> 0;
};

/**
 * Pick deterministically from an array using index + seed.
 * @template T
 * @param {T[]} arr
 * @param {number} index
 * @param {number} [seed=0]
 * @returns {T}
 */
export const pick = (arr, index, seed = 0) =>
  arr[hash(index, seed) % arr.length];

